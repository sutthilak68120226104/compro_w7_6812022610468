#include <stdio.h>  // เรียกใช้ไลบรารีมาตรฐานสำหรับ input/output

int main() {
    float score;    // ตัวแปรสำหรับเก็บคะแนนสอบที่ผู้ใช้กรอก
    float total;    // ตัวแปรสำหรับเก็บคะแนนรวม (หลังคำนวณเพิ่มพิเศษ ถ้ามี)

    // ขอให้ผู้ใช้ป้อนคะแนนสอบ midterm
    printf("Enter midterm score: ");
    scanf("%f", &score);  // รับค่าที่ผู้ใช้ป้อนมาเก็บไว้ในตัวแปร score

    // ตรวจสอบว่าคะแนนมากกว่าหรือเท่ากับ 50 หรือไม่
    if (score >= 50) {
        // ถ้าคะแนน >= 50 ให้บวกคะแนนเพิ่มพิเศษ 5%
        total = score + (score * 0.05);
    } else {
        // ถ้าคะแนน < 50 ก็ใช้คะแนนเดิม
        total = score;
    }

    // แสดงผลคะแนนรวม (หลังคำนวณเพิ่มถ้ามี)
    printf("Total score: %.2f\n", total);  // แสดงทศนิยม 2 ตำแหน่ง

    // แสดงข้อความสิ้นสุดการประเมิน
    printf("End of evaluation\n");

    return 0;  // จบโปรแกรม
}